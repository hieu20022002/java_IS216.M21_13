int iDatLich =1;
    private void jPanelDatLichMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanelDatLichMouseClicked
        // TODO add your handling code here:
        if(iDatLich==1){
            jPanelDatLich.setBackground(new Color(176,171,202));
            jLabelDatLich.setIcon(imgDatLich);
            iDatLich++;

            resetBackground(jLabelFeedback,jPanelFeedback, imgFeedback_trang);
            
            iFeedback =1;
        }
        else if (iDatLich==2){
            iDatLich=2;
            this.toBack();
            DatLich manHinhDatLich = new DatLich();
            manHinhDatLich.setVisible(true);
            manHinhDatLich.toFront();
        }

    }//GEN-LAST:event_jPanelDatLichMouseClicked
int iFeedback =1;
    private void jPanelFeedbackMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanelFeedbackMouseClicked
        // TODO add your handling code here:
        if(iFeedback==1){
            jPanelFeedback.setBackground(new Color(170,233,229));
            jLabelFeedback.setIcon(imgFeedback);
            iFeedback++;
            
            resetBackground(jLabelDatLich,jPanelDatLich, imgDatLich_trang);
            
            iDatLich =1;

        }
        else if (iFeedback==2){
            iFeedback=2;
            this.toBack();
            ThemFeedback manHinhFeedback = new ThemFeedback();
            manHinhFeedback.setVisible(true);
            manHinhFeedback.toFront();
        }
    }//GEN-LAST:event_jPanelFeedbackMouseClicked

    private void jLabelLogOutMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelLogOutMouseClicked
        // TODO add your handling code here:
        int confirm =JOptionPane.showConfirmDialog(this, "Bạn muốn đăng xuất?", "Xác nhận",
        JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE); 
        if(confirm == JOptionPane.YES_OPTION){
        
            DangNhap manHinhDangNhap = new DangNhap();
            manHinhDangNhap.show();
            dispose();
        }
    }//GEN-LAST:event_jLabelLogOutMouseClicked

