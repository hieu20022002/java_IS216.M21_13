    private void btTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTimKiemActionPerformed
        String gtri = this.txtTimKiem.getText();
        if (gtri.length() > 0) {
            String sql = "select * from FIELD where FIELDID like '%" + gtri + "%' " ;
            try ( Connection con = OracleConnect.GetOracleConnect()) {
                // Tạo đối tượng thực thi câu lệnh Select
                PreparedStatement prest = con.prepareStatement(sql);
                ResultSet rs = prest.executeQuery(sql);
                if (rs.next()) {
                    tblModelTT.getDataVector().removeAllElements();
                    while (rs.next()) {
                        String row[] = new String[6];
                        row[0] = rs.getString(1);
                        row[1] = rs.getString(2);
                        row[2] = rs.getString(3);
                        row[3] = rs.getString(4);
                        row[4] = rs.getString(5);
                        row[5] = rs.getString(6);
                        tblModelTT.addRow(row);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Không tìm thấy sân");
                }
            } catch (Exception e) {
                System.out.print(e);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Hãy nhập mã sân để tìm kiếm");
        }
    }//GEN-LAST:event_btTimKiemActionPerformed

