    public void TaoTableField(){
        tblModelTT = new DefaultTableModel();
        String tieuDe[]={"Mã sân","Tên sân","Kích thước","Mức giá","Tình trạng",
            "Ghi chú"};
        tblModelTT.setColumnIdentifiers(tieuDe);
        try(Connection con = ConnectionUtils.getMyConnection()){
            String row[] = new String [6];
            String strSQL = "Select * from FIELD";
            Statement stat = con.createStatement();
            ResultSet rs = stat.executeQuery(strSQL);
            while (rs.next()){
                row[0] = rs.getString(1);
                row[1] = rs.getString(2);
                row[2] = rs.getString(3);
                row[3] = rs.getString(4);
                row[4] = rs.getString(5);
                row[5] = rs.getString(6);
                tblModelTT.addRow(row);
            }
        }
        catch (Exception e) {
            System.out.print(e);
        }
     tbField.setModel(tblModelTT);
     setVisible(true);
}
