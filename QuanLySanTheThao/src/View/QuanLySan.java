    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        // TODO add your handling code here:
        int indexTB = tbField.getSelectedRow();
        int ret = JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa", "Xóa thông"
            + "tin sân", JOptionPane.YES_NO_OPTION);
        if (ret == JOptionPane.YES_OPTION) {
            SanTheThao san = new SanTheThao();
            int countRecord = san.XoaSan(txtMaSan.getText());
            if (countRecord > 0) {
                JOptionPane.showMessageDialog(this, "Xóa thông tin sân thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thông tin sân thất bại");
            }
            if (indexTB < tblModelTT.getRowCount() && indexTB >= 0) {
                tblModelTT.removeRow(indexTB);
            }
        }
    }//GEN-LAST:event_btXoaActionPerformed

    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        // TODO add your handling code here:
        int ret = JOptionPane.showConfirmDialog(null,"Bạn có muốn sửa dữ liệu",
            "Sửa dữ liệu",JOptionPane.YES_NO_OPTION);
        if (ret == JOptionPane.YES_OPTION) {
            String masan = txtMaSan.getText();
            String tensan = txtTenSan.getText();
            String kichthuoc = txtKichThuoc.getText();
            String mucgia = txtMucGia.getText();
            String tinhtrang = txtTinhTrang.getText();
            String ghichu = txtGhiChu.getText();
            int indexTB = tbField.getSelectedRow();

            if (masan.equals(tblModelTT.getValueAt(indexTB, 0))) {
                SanTheThao san = new SanTheThao();
                //Lấy kết quả từ CSDL
                int countRecord = san.SuaSan(masan, tensan, kichthuoc, mucgia, tinhtrang, ghichu);
                if(countRecord>0){
                    JOptionPane.showMessageDialog(this, "Sửa thông tin sân thành công!");
                }
                else{
                    JOptionPane.showMessageDialog(this, "Sửa thông tin sân thất bại!");
                }
                if (indexTB < tblModelTT.getRowCount() && indexTB >= 0) {
                    tblModelTT.setValueAt(txtTenSan.getText(), indexTB, 1);
                    tblModelTT.setValueAt(txtKichThuoc.getText(), indexTB, 2);
                    tblModelTT.setValueAt(txtMucGia.getText(), indexTB, 3);
                    tblModelTT.setValueAt(txtTinhTrang.getText(), indexTB, 4);
                    tblModelTT.setValueAt(txtGhiChu.getText(), indexTB, 5);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Không được sửa mã sân");
            }
        }
    }//GEN-LAST:event_btSuaActionPerformed

