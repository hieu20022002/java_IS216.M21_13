    private void tbFieldMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbFieldMouseClicked
        // TODO add your handling code here:
        int indexTB = tbField.getSelectedRow();
        if (indexTB < tblModelTT.getRowCount() && indexTB >=0)
        txtMaSan.setText(tblModelTT.getValueAt(indexTB, 0).toString());
        txtTenSan.setText(tblModelTT.getValueAt(indexTB, 1).toString());
        txtKichThuoc.setText(tblModelTT.getValueAt(indexTB, 2).toString());
        txtMucGia.setText(tblModelTT.getValueAt(indexTB, 3).toString());
        txtTinhTrang.setText(tblModelTT.getValueAt(indexTB, 4).toString());
        txtGhiChu.setText(tblModelTT.getValueAt(indexTB, 5).toString());
    }//GEN-LAST:event_tbFieldMouseClicked

