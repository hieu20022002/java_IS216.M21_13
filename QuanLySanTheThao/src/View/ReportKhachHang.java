    private void LayThongTinThongKe(String thang1, String thang2 ,String nam1, String nam2) throws SQLException{
        HashMap hs = new HashMap();
        hs.put("FromMonth", thang1);
        hs.put("ToMonth", thang2);
        hs.put("FromYear", nam1);
        hs.put("ToYear", nam2);
        String localDir = System.getProperty("user.dir");
        ReportViewer viewer = new ReportViewer(localDir + "\\src\\Resources\\ThongKeDoanhThuTheoKhachHang.jrxml", hs);
        viewer.setVisible(true);
    }
