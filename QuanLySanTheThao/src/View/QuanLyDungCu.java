    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        // TODO add your handling code here:
        int ret = JOptionPane.showConfirmDialog(null,"Bạn có muốn sửa dữ liệu",
            "Sửa dữ liệu",JOptionPane.YES_NO_OPTION);
        if (ret == JOptionPane.YES_OPTION) {
            String madungcu = txtMaDungCu.getText();
            String tendungcu = txtTenDungCu.getText();
            String slconlai = txtSLConLai.getText();
            String mucgia = txtMucGia.getText();
            String ghichu = txtGhiChu.getText();
            int indexTB = tbDungCu.getSelectedRow();

            if (madungcu.equals(tblModelTT.getValueAt(indexTB, 0))) {
                DungCu dc = new DungCu();
                //Lấy kết quả từ CSDL
                int countRecord = dc.SuaDungCu(madungcu, tendungcu, slconlai, mucgia, ghichu);
                if(countRecord>0){
                    JOptionPane.showMessageDialog(this, "Sửa thông tin dụng cụ thành công!");
                }
                else{
                    JOptionPane.showMessageDialog(this, "Sửa thông tin dụng cụ thất bại!");
                }
                if (indexTB < tblModelTT.getRowCount() && indexTB >= 0) {
                    tblModelTT.setValueAt(txtTenDungCu.getText(), indexTB, 1);
                    tblModelTT.setValueAt(txtSLConLai.getText(), indexTB, 2);
                    tblModelTT.setValueAt(txtMucGia.getText(), indexTB, 3);
                    tblModelTT.setValueAt(txtGhiChu.getText(), indexTB, 4);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Không được sửa mã dụng cụ");
            }
        }
    }//GEN-LAST:event_btSuaActionPerformed

